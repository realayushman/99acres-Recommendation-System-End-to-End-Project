def convert_to_rupees(row):
    num = float(row['price_num'])
    unit = row['price_unit']

    if unit in ['cr']:
        return num * 10000000
    elif unit in ['l']:
        return num * 100000
    else:
        return num  # already in rupees

# Apply conversion
