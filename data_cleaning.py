import re

# Function to split number and unit
def separate_number_text(s):
    s = str(s).strip().lower().replace(",", "")

    # Handle range: "5.2 - 6.3 cr"
    range_match = re.match(r'^([\d\.]+)\s*-\s*([\d\.]+)\s*([a-zA-Z]*)$', s)
    if range_match:
        n1, n2 = float(range_match.group(1)), float(range_match.group(2))
        avg = (n1 + n2) / 2
        unit = range_match.group(3) if range_match.group(3) else None
        return str(avg), unit

    # Handle normal values: "3cr", "85,000", etc.
    match = re.match(r'^([\d\.]+)\s*([a-zA-Z]*)$', s)
    if match:
        number = match.group(1)
        unit = match.group(2) if match.group(2) else None
        return number, unit


